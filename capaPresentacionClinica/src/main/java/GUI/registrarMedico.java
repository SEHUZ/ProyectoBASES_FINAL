    public void registrarMedico() throws PersistenciaClinicaException {
        try {
            String nombre = fieldNombre.getText();
            String apellidoPaterno = fieldApellidoPaterno.getText();
            String apellidoMaterno = fieldApellidoMaterno.getText();
            String contrasenia = jContraseniaField.getText();
            String cedula = fieldCedula.getText();
            String especialidad = fieldEspecialidad.getText();
            String User = fieldUser.getText();

            Usuario usuario = new Usuario(User, contrasenia, "Medico");
            MedicoNuevoDTO medico = new MedicoNuevoDTO(usuario, nombre, apellidoPaterno, apellidoMaterno, cedula, especialidad, true);

            boolean exito = medicoBO.registrarMedico(medico);
            if (exito) {
                JOptionPane.showMessageDialog(this, "El medico se ha registrado exitosamente");
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "ERROR al registrar al medico");
            }
        } catch (NegocioException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }

    public void limpiarCampos() {
        // Limpiar campos de texto
        fieldNombre.setText("");
        fieldApellidoPaterno.setText("");
        fieldApellidoMaterno.setText("");
        jContraseniaField.setText("");
        fieldCedula.setText("");
        fieldEspecialidad.setText("");
        fieldUser.setText("");
    }
